#include "3-calc.h"
/**
 * op_add - function that adds two numbers.
 *
 * @a: first number.
 *
 * @b: second number.
 *
 * Return: sum of a and b.
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function that performs subtraction of two numbers.
 *
 *@a: first number.
 *
 * @b: second number.
 *
 * Return: difference of a and b.
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function that multiplies two numbers.
 *
 * @a: first number.
 *
 * @b: second number.
 *
 * Return: product of a and b.
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function that performs the division of two numbers.
 *
 * @a: first number.
 *
 * @b: second number.
 *
 * Return: result of a divide by b.
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function that finds the remainder after dividing two numbers.
 *
 * @a: first number.
 *
 * @b: second number.
 *
 * Return: the remainder after dividing a by b.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
