#include <stdio.h>

/**
 * add - Adds two integers.
 * @a: The first integer.
 * @b: The second integer.
 *
 * Return: The result of the addition (a + b).
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - Subtracts two integers.
 * @a: The first integer.
 * @b: The second integer.
 *
 * Return: The result of the subtraction (a - b).
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - Multiplies two integers.
 * @a: The first integer.
 * @b: The second integer.
 *
 * Return: The result of the multiplication (a * b).
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - Divides two integers.
 * @a: The numerator.
 * @b: The denominator.
 *
 * Return: The result of the division (a / b).
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - Computes the remainder of the division of two integers.
 * @a: The dividend.
 * @b: The divisor.
 *
 * Return: The remainder of the division (a % b).
 */
int mod(int a, int b)
{
	return (a % b);
}
